package math;

        public class if4 {

	    public static void main(String[] args) {
	    	
		System.out.println( (10>5) && (12>11) ); // true
		 
	    System.out.println( (10>5) && (12<11) ); // false
		
		// or operator
		
		System.out.println( (10>5) ||  (13<12) ); // true
		
		System.out.println( (10<5) ||  (13<12) ); // false
		
		// nor operator !
		
		System.out.println( !(12==11) ); // true
		
		System.out.println (! (10>4)); // false
		
		
		

	}

}
